#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t i;

	i = 0;
	while (i++ < n && *src)
		*dst++ = *src++;
	i--;
	while (i++ < n)
		*dst++ = '\0';
	return (dst -= n);
}
